public class Music {
    String name;
    String artist;
    String genre;
    int year;
    int bpm;
    int nrgy;
    int dnce;

    public Music(String name, String artist, String genre, int year, int bpm, int nrgy, int dnce) {
        this.name = name;
        this.artist = artist;
        this.genre = genre;
        this.year = year;
        this.bpm = bpm;
        this.nrgy = nrgy;
        this.dnce = dnce;
    }
}
